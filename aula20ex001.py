def soma(a, b):
    s = a + b
    print(s)


# Programa Principal
# a = 4
# b = 8
# s = a + b
# print(s)
soma(4, 8)

# a = 1
# b = 6
# s = a + b
# print(s)
soma(a = 1, b = 6)      # Pode explicitar quem são as variáveis

# a = 7
# b = 9
# s = a + b
# print(s)
soma(b = 7, a = 9)      # Pode até mudar a ordem das variáveis, explicitando-as
